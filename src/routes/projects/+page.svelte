<article>
	<h4>
		<a href="https://pocketbase.io/" target="_blank">Pocketbase: a backend in one single file!</a>
	</h4>
	<p>
		Pocketbase is a self hosted DB, you can find the source code <a
			href="https://github.com/pocketbase/pocketbase"
			target="_blank">here</a
		>. With Pocketbase, you can create a production-ready backend in less than 5 minutes, and it is
		very easy! You heard me right, a single file. The magic behind this project is Go, compiled and
		built to use SQLite as it’s database with a REST-ish API that packs a whole set of features. At
		the time of writing, a Javascript SDK exists to easily add on top with Svelte, React or what
		ever other front-end framework you like to use.
	</p>
	<ul>
		<li>Real time database (Powered by SQLite)</li>
		<li>File Storage — Integrates with S3, more Object Storage support is coming.</li>
		<li>Authentication</li>
		<li>Full Featured Admin UI — To manage users, collections & more</li>
		<li>Extendable via Go Programming Language to extend functionality</li>
	</ul>
</article>

